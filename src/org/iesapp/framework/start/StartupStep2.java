    public boolean isValidDatabase()
    {
        boolean q = false;
        if(jRadioButton2.isSelected())
        {
            if(!jTextField2.getText().trim().isEmpty()) 
            {
                String txt =  jTextField2.getText().trim().replaceAll(" ", "_");
                if(tmpClient.getMysql().setCatalog(txt)==1)
                {
                    q = false;
                   //Error this database already exits
                    JOptionPane.showMessageDialog(javar.JRDialog.getActiveFrame(), "Aquesta base de dades ja existeix. Trieu un altre nom");
                }
                else
                {
                    q = true;
                }
            }
        }
        else
        {
            if(jList1.getSelectedIndex()>=0) {
                q=true;
            }
        }
        return q;
    }
    
    public boolean saveToFile()
    {
        boolean q = true;
        
        if(jRadioButton2.isSelected())
        {
            CoreCfg.core_mysqlDBPrefix =  jTextField2.getText().trim().replaceAll(" ", "_");
            if(tmpClient.getMysql().setCatalog(CoreCfg.core_mysqlDBPrefix)==1)
            {
                q = false;
                //Error this database already exits
                JOptionPane.showMessageDialog(javar.JRDialog.getActiveFrame(), "Aquesta base de dades ja existeix. Trieu un altre nom");
            }
        }
        else
        {
            CoreCfg.core_mysqlDBPrefix =  (String) jList1.getSelectedValue();
        }        
        
        if(q) 
        {
            //Saves the encrypted file
            CoreCfg.saveEncryptedFile();
            //Create a blank database 
            if(jRadioButton2.isSelected()) {
                tmpClient.createEmptyDB(CoreCfg.core_mysqlDBPrefix);
            }
        }
        
        return q;
    }
    
