    public void loadDBsInHost()
    {
        
        String SQL = "Show databases";
        if(tmpClient.getMysql().isClosed())
        {
            System.out.println("ERROR: connection is lost");
            return;
        }
        
        try {
            Statement st = tmpClient.getMysql().createStatement();
            ResultSet rs1 = tmpClient.getMysql().getResultSet(SQL,st);
            while(rs1!=null && rs1.next())
            {
                listmodel1.addElement(rs1.getString(1));
            }
            if(rs1!=null) {
                rs1.close();
                st.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(StartupStep2.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jList1.setModel(listmodel1);
        
        if( CoreCfg.core_mysqlDBPrefix.isEmpty()) 
        {
            jTextField2.setText("");
        }
        else
        {
            jTextField2.setText(CoreCfg.core_mysqlDBPrefix);
            jRadioButton1.setSelected(true);
            jList1.setSelectedValue(CoreCfg.core_mysqlDBPrefix, true);
        }
        int indexOf = listmodel1.indexOf("iesdigital_demo");
        if(StartupWiz.configType==StartupStep0.PORTABLE_MYSQL && indexOf>=0)
        {
            jRadioButton1.setSelected(true);
            jList1.setSelectedIndex(indexOf);
        }
    }
    
