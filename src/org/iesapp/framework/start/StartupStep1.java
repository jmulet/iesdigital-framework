    public boolean tryConnection() {
     
        boolean q = MyDatabase.tryConnection(jhost.getText().trim(), "", juser.getText().trim(), 
                new String(jpwd.getPassword()).trim(), jParams.getText().trim(), false);
        //Create a temporary connection to database
        return q;
    }
    
    public void saveConnection() {
        //System.out.println("saving");
        CoreCfg.core_mysqlDB = ""; //not yet specified
        CoreCfg.core_mysqlHost = jhost.getText().trim();
        CoreCfg.core_mysqlUser = juser.getText().trim();
        CoreCfg.core_mysqlPasswd = new String(jpwd.getPassword()).trim();
        CoreCfg.coreDB_mysqlParam = jParams.getText().trim();
        CoreCfg.saveEncryptedFile();
        boolean startFitxesDB = StartupWiz.tmpClient.startFitxesDB();
        System.out.println(startFitxesDB+" -- "+StartupWiz.tmpClient.getMysql());
       
    }
