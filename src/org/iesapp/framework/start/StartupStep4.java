    public void onShow()
    {
        jHost.setText(CoreCfg.configTableMap.get("sgdHost").toString());
        jUser.setText(CoreCfg.configTableMap.get("sgdUser").toString());
        jPwd.setText(CoreCfg.configTableMap.get("sgdPasswd").toString());
        jDb.setText(CoreCfg.configTableMap.get("sgdDBPrefix").toString());
        jParams.setText(CoreCfg.configTableMap.get("sgdDBParams").toString());
    }
    
    public void doSave() {
       CoreCfg.configTableMap.put("sgdHost", jHost.getText().trim());
       CoreCfg.configTableMap.put("sgdUser", jUser.getText().trim());
       CoreCfg.configTableMap.put("sgdPasswd", new String(jPwd.getPassword()).trim());
       CoreCfg.configTableMap.put("sgdDBPrefix", jDb.getText().trim());
       CoreCfg.configTableMap.put("sgdDBParams", jParams.getText().trim());
       tmpClient.updateDatabaseCfg(); //commit to db
    }
    
