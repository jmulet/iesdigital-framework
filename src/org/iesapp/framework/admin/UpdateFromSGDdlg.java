    public UpdateFromSGDdlg(java.awt.Frame parent, boolean modal, String invokeUser, CoreCfg coreCfg) {
        super(parent, modal);
        this.coreCfg = coreCfg;
        initComponents();
        this.invokeUser = invokeUser;

        sgd = new MyDatabase(CoreCfg.coreDB_sgdHost, CoreCfg.coreDB_sgdDB,
                CoreCfg.coreDB_sgdUser, CoreCfg.coreDB_sgdPasswd, CoreCfg.coreDB_sgdParam);
        boolean q = sgd.connect();
        if(!q)
        {
            SgdConfig dlg = new SgdConfig(parent, true, coreCfg);
            dlg.setLocationRelativeTo(null);
            dlg.setVisible(true);
            if(dlg.Ok)
            {
                 sgd = new MyDatabase(CoreCfg.coreDB_sgdHost, CoreCfg.coreDB_sgdDB,
                 CoreCfg.coreDB_sgdUser, CoreCfg.coreDB_sgdPasswd, CoreCfg.coreDB_sgdParam);
                 q = sgd.connect();
            }
            else
            {
                jButton1.setEnabled(false);
            }
        }

        modelCombo1 = new DefaultComboBoxModel();
      
        if(q)
        {
            String SQL1 = "SHOW DATABASES";
            try {
                Statement st = sgd.createStatement();
                ResultSet rs1 = sgd.getResultSet(SQL1,st); 
                while (rs1.next()) {
                    String txt = rs1.getString("Database");
                    if(txt.startsWith((String) CoreCfg.configTableMap.get("sgdDBPrefix")) && !txt.contains("tmp")){
                          modelCombo1.addElement(txt);
                    }
                }
                if(rs1!=null)
                {
                    rs1.close();
                    st.close();
                }
            } catch (SQLException ex) {
                Logger.getLogger(UpdateFromSGDdlg.class.getName()).log(Level.SEVERE, null, ex);
            }

 
            jComboBox1.setModel(modelCombo1);
            jComboBox1.setSelectedIndex(modelCombo1.getSize()-1);
          
            doConnect();
            setAvaluacions();
            setLlistesCodigs();
        }
        
        timer = new javax.swing.Timer(AnimationRate, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (task != null && task.isAlive()) {
                    jButton1.setEnabled(false);
                    jButton2.setEnabled(false);
                    jProgressBar1.setValue((int) task.getStatus());
                    jTextArea1.append(task.getLogoutput());
                } 
                else {
                    jButton1.setEnabled(true);
                    jButton2.setEnabled(true);
                    jTextArea1.setText("");
                    jTextArea1.append(task.getLogoutput());
                    timer.stop();
                    jProgressBar1.setValue(100);
                }
            }
        });


    }

