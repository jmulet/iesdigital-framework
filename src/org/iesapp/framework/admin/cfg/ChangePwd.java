    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String pwd = new String(jAntic.getPassword());
        String nou = new String(jNou.getPassword());
        String nou2 = new String(jNou2.getPassword());

        if(!CoreCfg.core_pwdSU.equals(pwd))
        {
              JTextArea area =new JTextArea();
              area.setWrapStyleWord(true);
              area.setText(bundle.getString("incorrect"));
              area.setLineWrap(true);
              JOptionPane.showMessageDialog(javar.JRDialog.getActiveFrame(), area, "Error", JOptionPane.ERROR_MESSAGE);
              return;
        }

        if(!nou.equals(nou2))
        {

              JTextArea area =new JTextArea();
              area.setText(bundle.getString("nomatch"));
              area.setLineWrap(true);
              area.setWrapStyleWord(true);
              JOptionPane.showMessageDialog(javar.JRDialog.getActiveFrame(), area, "Error", JOptionPane.ERROR_MESSAGE);
              return;
            
        }

         JTextArea area =new JTextArea();
         area.setText(bundle.getString("changed"));
         area.setLineWrap(true);
         JOptionPane.showMessageDialog(javar.JRDialog.getActiveFrame(), area, "Confirmation", JOptionPane.INFORMATION_MESSAGE);

         CoreCfg.core_pwdSU = nou;
         CoreCfg.configTableMap.put("adminPwd", nou);
         coreCfg.updateDatabaseCfg("adminPwd", nou);
         this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

