    public void setIniParams(HashMap<String,Object> params) {
        
        if(params.containsKey("height"))
        {
            int h = Integer.parseInt((String) params.get("height"));
            this.setPreferredSize(new Dimension(-1,h));
            this.revalidate();
        }
    }
    
