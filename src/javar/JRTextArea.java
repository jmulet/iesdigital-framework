    public boolean check() {
         boolean check = true;        
        if (required) {
            check = !getValue().trim().isEmpty();
        }
         if(!check)
        {
            jLabel1.setBackground(Color.red);
            jLabel1.setOpaque(true);
        }
        else
        {
            jLabel1.setOpaque(false);
        }
        return check;
    }

